    def __init__(self):
        self.acupoint_patterns = []
        for name in KNOWN_ACUPOINTS.get("英文", []):
            self.acupoint_patterns.append((name, name))
        for name in KNOWN_ACUPOINTS.get("中文", []):
            self.acupoint_patterns.append((name, name))

    def extract_acupoints(self, text: str) -> list:
        """从文本中提取穴位名"""
        if not text:
            return []
        text_upper = text.upper()
        found = set()
        for pattern, name in self.acupoint_patterns:
            if pattern.upper() in text_upper:
                found.add(name)
        return sorted(list(found))

    def extract_genes(self, text: str) -> list:
        """从文本中提取可能的基因名"""
        if not text:
            return []
        candidates = set()
        text_upper = text.upper()

        # 方法1: 匹配已知基因（同时处理带连字符的版本如 IL-6）
        for gene in self.KNOWN_GENES:
            # 匹配无连字符版本
            pattern = r'\b' + re.escape(gene) + r'\b'
            if re.search(pattern, text_upper):
                candidates.add(gene)
            # 匹配带连字符版本（如 IL-6, TNF-alpha）
            if re.search(r'\b' + re.escape(gene) + r'[-_]\w+\b', text_upper):
                candidates.add(gene)
            # 如果基因本身包含数字，也匹配连字符插入形式（如 IL6 -> IL-6）
            if any(c.isdigit() for c in gene):
                hyphenated = re.sub(r'(\D)(\d)', r'\1-\2', gene)
                if re.search(r'\b' + re.escape(hyphenated) + r'\b', text_upper):
                    candidates.add(gene)

        # 方法2: 匹配大写字母+数字模式（预处理：替换连字符和下划线为空格，避免RT-QPCR被整体匹配）
        text_processed = text.replace('-', ' ').replace('_', ' ')
        for match in self.GENE_PATTERN.findall(text_processed):
            word = match.upper()
            if word in self.NON_GENE_WORDS:
                continue
            if len(word) < 2 or len(word) > 15:
                continue
            if word.isdigit():
                continue
            # 过滤掉只包含一个字母其余是数字的
            if sum(1 for c in word if c.isalpha()) == 1 and sum(1 for c in word if c.isdigit()) >= 3:
                continue
            has_digit = any(c.isdigit() for c in word)
            has_suffix = word.endswith(("A", "B", "C", "D", "G", "L", "R", "P", "S", "T", "K", "M", "N", "H", "E", "F"))
            if has_digit or has_suffix:
                candidates.add(word)

        return sorted(list(candidates))

