def batch_extract(db_path: str):
    """批量处理数据库中所有文章的实体提取"""
    import sqlite3
    from config import DB_PATH

    db_path = db_path or DB_PATH
    conn = sqlite3.connect(db_path)
    c = conn.cursor()
    extractor = EntityExtractor()

    c.execute("SELECT id, title, abstract FROM articles")
    rows = c.fetchall()

    print(f"[EXTRACT] 处理 {len(rows)} 篇文章...")
    acupoint_inserts = []
    gene_inserts = []
    disease_inserts = []

    for row in rows:
        article_id, title, abstract = row
        result = extractor.analyze_article(title or "", abstract or "")

        for apt in result["acupoints"]:
            acupoint_inserts.append((article_id, apt))
        for gene in result["genes"]:
            gene_inserts.append((article_id, gene))
        for disease in result["diseases"]:
            disease_inserts.append((article_id, disease))

    for article_id, apt_name in acupoint_inserts:
        c.execute("INSERT OR IGNORE INTO acupoints (name_en) VALUES (?)", (apt_name,))
        c.execute("SELECT id FROM acupoints WHERE name_en=?", (apt_name,))
        apt_id = c.fetchone()[0]
        c.execute("INSERT OR IGNORE INTO article_acupoint (article_id, acupoint_id) VALUES (?,?)",
                  (article_id, apt_id))

    for article_id, gene_sym in gene_inserts:
        c.execute("INSERT OR IGNORE INTO genes (gene_symbol) VALUES (?)", (gene_sym,))
        c.execute("SELECT id FROM genes WHERE gene_symbol=?", (gene_sym,))
        gene_id = c.fetchone()[0]
        c.execute("INSERT OR IGNORE INTO article_gene (article_id, gene_id) VALUES (?,?)",
                  (article_id, gene_id))

    for article_id, disease_name in disease_inserts:
        c.execute("INSERT OR IGNORE INTO diseases (name) VALUES (?)", (disease_name,))
        c.execute("SELECT id FROM diseases WHERE name=?", (disease_name,))
        disease_id = c.fetchone()[0]
        c.execute("INSERT OR IGNORE INTO article_disease (article_id, disease_id) VALUES (?,?)",
                  (article_id, disease_id))

    conn.commit()
    conn.close()
